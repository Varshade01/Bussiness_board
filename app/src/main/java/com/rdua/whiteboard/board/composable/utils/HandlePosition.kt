package com.rdua.whiteboard.board.composable.utils

/**
 * Defines different handle positions relative to the UI element.
 *
 * - [TOP_START] - handle in top start position.
 * - [TOP_END] - handle in top end position.
 * - [MIDDLE_START] - handle in middle start position.
 * - [MIDDLE_END] - handle in middle end position.
 * - [BOTTOM_START] - handle in bottom start position.
 * - [BOTTOM_END] - handle in bottom end position.
 * - [START] - handle in start centre position.
 * - [MIDDLE] - handle in middle centre position.
 * - [END] - handle in end centre position.
 */
internal enum class HandlePosition {
    TOP_START, TOP_END, MIDDLE_START, MIDDLE_END, BOTTOM_START, BOTTOM_END, START, MIDDLE, END
}
